    # Sample SHAP features
    sample_features = [
        {
            'feature_name': 'resting_hr_estimate',
            'feature_display_name': 'Resting Heart Rate',
            'value': 92.5,
            'unit': 'bpm',
            'impact': 'INCREASES',
            'shap_value': 0.25
        },
        {
            'feature_name': 'sleep_hours_avg',
            'feature_display_name': 'Average Sleep Duration',
            'value': 5.2,
            'unit': 'hours',
            'impact': 'INCREASES',
            'shap_value': 0.18
        },
        {
            'feature_name': 'activity_score',
            'feature_display_name': 'Physical Activity Score',
            'value': 18.5,
            'unit': 'points',
            'impact': 'INCREASES',
            'shap_value': 0.15
        }
    ]

    # Sample observation IDs
    obs_ids = ['obs-123', 'obs-124', 'obs-125']

    print("=== Creating FHIR RiskAssessment ===\n")

    # Create RiskAssessment
    risk_assessment = create_risk_assessment(
        user_id=123,
        ml_score=0.72,
        risk_level=RISK_LEVEL_RED,
        top_features=sample_features,
        observation_ids=obs_ids
    )

    print("RiskAssessment created:")
    print(f"  Patient: {risk_assessment.subject.reference}")
    print(f"  Probability: {risk_assessment.prediction[0].probabilityDecimal}")
    print(f"  Risk: {risk_assessment.prediction[0].qualitativeRisk.text}")
    print(f"  Basis: {len(risk_assessment.basis)} observations" if risk_assessment.basis else "  No basis observations")
    print(f"  Notes: {len(risk_assessment.note)}" if risk_assessment.note else "  No notes")

    print(f"\nRiskAssessment JSON:")
    print(risk_assessment_to_json(risk_assessment))

    print("\n" + "="*60)
    print("=== Creating FHIR Flag ===\n")

    # Create Flag
    flag = create_risk_flag(
        user_id=123,
        risk_level=RISK_LEVEL_RED,
        reason="Multiple cardiovascular wellness indicators warrant attention"
    )

    if flag:
        print("Flag created:")
        print(f"  Patient: {flag.subject.reference}")
        print(f"  Code: {flag.code.text}")
        print(f"  Status: {flag.status}")
        print(f"  Category: {flag.category[0].coding[0].display}")

        print(f"\nFlag JSON:")
        print(flag_to_json(flag))
    else:
        print("No flag created (low risk)")

    # Test Green risk (no flag)
    print("\n" + "="*60)
    print("=== Testing Green Risk (No Flag) ===\n")

    flag_green = create_risk_flag(
        user_id=456,
        risk_level=RISK_LEVEL_GREEN,
        reason="Low risk"
    )

    if flag_green:
        print("Flag created (unexpected)")
    else:
        print("✓ No flag created for low risk patient (as expected)")
